package com.incluctab.tddClasses.chapterTwo;

public class IntegerValueOfCharacter {
    public static void main(String[] args) {
        System.out.println("The integer value of A is " +((int) 'A'));
        System.out.println("The integer value of B is " +((int) 'B'));
        System.out.println("The integer value of C is " +((int) 'C'));
        System.out.println("The integer value of a is " +((int) 'a'));
        System.out.println("The integer value of b is " +((int) 'b'));
        System.out.println("The integer value of c is " +((int) 'c'));
        System.out.println("The integer value of 0 is " +((int) '0'));
        System.out.println("The integer value of 1 is " +((int) '1'));
        System.out.println("The integer value of 2 is " +((int) '2'));
        System.out.println("The integer value of $ is " +((int) '$'));
        System.out.println("The integer value of * is " +((int) '*'));
        System.out.println("The integer value of + is " +((int) '+'));
        System.out.println("The integer value of / is " +((int) '/'));
    }
}
